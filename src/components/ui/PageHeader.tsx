'use client'

import { ReactNode } from 'react'
import Button from './Button'

interface Stat {
  label: string
  value: string | number
}

interface ActionProps {
  label: string
  onClick: () => void
  icon?: ReactNode
}

interface PageHeaderProps {
  title: string
  subtitle: string
  icon: ReactNode
  stats?: Stat[]
  action?: ActionProps
  secondaryAction?: ActionProps
  children?: ReactNode
}

export default function PageHeader({
  title,
  subtitle,
  icon,
  stats,
  action,
  secondaryAction,
  children,
}: PageHeaderProps) {
  return (
    <div className="bg-gradient-to-r from-blue-600 to-blue-400 rounded-2xl px-6 sm:px-8 py-6 sm:py-8 shadow-lg">
      <div className="flex flex-col sm:flex-row sm:items-start sm:justify-between gap-4">
        <div className="text-white">
          <div className="flex items-center gap-3 mb-2">
            <div className="p-2 bg-white/20 rounded-lg backdrop-blur-sm">
              {icon}
            </div>
            <h1 className="text-xl sm:text-2xl font-bold">{title}</h1>
          </div>
          <p className="text-blue-100 text-sm ml-0 sm:ml-12">{subtitle}</p>

          {/* Quick Stats */}
          {stats && stats.length > 0 && (
            <div className="flex flex-wrap gap-4 sm:gap-6 mt-4 sm:mt-6 sm:ml-12">
              {stats.map((stat) => (
                <div key={stat.label} className="text-white">
                  <div className="text-xl sm:text-2xl font-bold">{stat.value}</div>
                  <div className="text-blue-200 text-xs uppercase tracking-wide">{stat.label}</div>
                </div>
              ))}
            </div>
          )}

          {children}
        </div>

        {(action || secondaryAction) && (
          <div className="flex flex-col sm:flex-row gap-2 w-full sm:w-auto">
            {secondaryAction && (
              <Button
                onClick={secondaryAction.onClick}
                className="bg-white/20 text-white hover:bg-white/30 border-0 backdrop-blur-sm w-full sm:w-auto"
              >
                {secondaryAction.icon}
                {secondaryAction.label}
              </Button>
            )}
            {action && (
              <Button
                onClick={action.onClick}
                className="bg-white text-blue-600 hover:bg-blue-50 border-0 shadow-lg w-full sm:w-auto"
              >
                {action.icon}
                {action.label}
              </Button>
            )}
          </div>
        )}
      </div>
    </div>
  )
}

